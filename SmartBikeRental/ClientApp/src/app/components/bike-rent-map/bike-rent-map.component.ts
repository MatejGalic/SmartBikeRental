import { Component, OnDestroy, OnInit, Renderer2 } from '@angular/core';
import * as L from 'leaflet';
import { Subject, filter, take, takeUntil } from 'rxjs';
import { DeviceDto } from 'src/app/core/models/autogenerated/dtos/deviceDto';
import { SignalRService } from 'src/app/core/services/signal-r.service';

@Component({
  selector: 'app-bike-rent-map',
  templateUrl: './bike-rent-map.component.html',
  styleUrls: ['./bike-rent-map.component.scss'],
})
export class BikeRentMapComponent implements OnInit, OnDestroy {
  private readonly LAT_DEFAULT = 45.8153;
  private readonly LNG_DEFAULT = 15.9665;
  private map: L.Map;
  private popups: L.Popup[] = [];
  private destroy$: Subject<void> = new Subject<void>();

  constructor(private signalR: SignalRService, private renderer: Renderer2) {}

  ngOnDestroy(): void {
    this.destroy$.next();
    this.destroy$.complete();
  }

  ngOnInit(): void {
    this.signalR.devices$
      .pipe(takeUntil(this.destroy$))
      .subscribe((devices) => {
        // update device info in popup
        this.updatePopups(devices);
      });
  }

  public onMapReady(map: L.Map) {
    this.map = map;

    // set initial device data on map
    this.signalR.devices$
      .pipe(
        filter((e) => !!e),
        take(1)
      )
      .subscribe((ds) => {
        ds.forEach((t) => {
          const p = L.popup({
            closeButton: false,
            autoClose: false,
            closeOnClick: false,
            closeOnEscapeKey: false,
          })
            .setLatLng([this.LAT_DEFAULT + 1, this.LNG_DEFAULT + 1])
            .setContent('loading data')
            .addTo(this.map)
            .openPopup();

          this.popups.push(p);
        });

        this.updatePopups(ds);
      });
  }

  private updatePopups(devices: DeviceDto[]) {
    this.popups.forEach((p, i) => {
      const device = devices[i];

      let pHtmlEl = p.getElement();
      let pContainerEl = pHtmlEl.children[0];
      let pPointerEl = pHtmlEl.children[1].children[0];

      [pContainerEl, pPointerEl].forEach((el) => {
        this.renderer.removeClass(el, '!bg-green-400');
        this.renderer.removeClass(el, '!bg-red-400');

        device.isLocked
          ? this.renderer.addClass(el, '!bg-red-400')
          : this.renderer.addClass(el, '!bg-green-400');
      });
      //  Status: <span style="font-weight: 600">${device.isLocked ? 'Taken' : 'Available'} </span>
      p.setContent(`
      <h1 class="text-base font-semibold">Device: #${i}</h1>
      <img class="w-10 h-10 mx-auto" src="assets/images/icons/${
        device.isLocked ? 'locked' : 'unlocked'
      }-dark.svg" />
       `);

      p.setLatLng([device.latitude, device.longitude]);
    });
  }

  public options: L.MapOptions = {
    layers: [
      L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        maxZoom: 18,
        minZoom: 1,
        attribution:
          'Map data Â© <a href="http://openstreetmap.org">OpenStreetMap</a> contributors',
      }),
    ],
    zoom: 7,
    center: L.latLng(this.LAT_DEFAULT, this.LNG_DEFAULT),
  };
}
